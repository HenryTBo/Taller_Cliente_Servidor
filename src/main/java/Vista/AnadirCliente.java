    /**
     * Boton para ingresar un cliente a la base de datos.
     * Toma los datos de los textfields presentes para completar la informacion necesario.
     * @param evt 
     */
    private void jB_IngresarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_IngresarClienteActionPerformed
        // TODO add your handling code here:
        try{
            if(registeredNameCliente.getText().isEmpty() || registeredNumeroCliente.getText().isEmpty() || registeredCorreoCliente.getText().isEmpty() ){
                JOptionPane.showMessageDialog(this,"Por favor no dejar espacios en blanco.");   
            }else{
            
                cliente.setCorreo(this.registeredCorreoCliente.getText());
                cliente.setNombre(this.registeredNameCliente.getText());
                cliente.setNumero(this.registeredNumeroCliente.getText());   
                ConexionCliente.insertarCliente(cliente); 
            }    
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al ingresar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jB_IngresarClienteActionPerformed
