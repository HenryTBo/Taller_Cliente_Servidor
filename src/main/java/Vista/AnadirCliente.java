    /**
     * Boton para eliminar cliente de la base datos por medio de ID.
     * @param evt 
     */
    private void jB_EliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_EliminarClienteActionPerformed
        // TODO add your handling code here:
        try{
            if(registeredIdCliente.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Por favor ingrese ID del Cliente.");
            }else{
                int idCliente = Integer.valueOf(registeredIdCliente.getText());
                ConexionCliente.eliminarCliente(idCliente);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al eliminar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jB_EliminarClienteActionPerformed


