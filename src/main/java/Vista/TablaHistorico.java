    /**
     * Metodo para cargar los datos de la base historico, crear un objeto Historico
     * para asi poder utilizar la informacion del objeto para rellenar la tabla.
     */
    private void cargarDatos() {
        List<Historico> historicos = conexionHistoricos.obtenerTablaHistoricos();
        DefaultTableModel tableModel = (DefaultTableModel) jTable_historicos.getModel();
        tableModel.setRowCount(0);
        
        for (Historico h : historicos) {
            tableModel.addRow(new Object[]{
                h.getIdFactura(),
                h.getFecha(),
                h.getNombreCliente(),
                h.getPlacaVehiculo(),
                h.getServicio(),
                h.getPrecio()
            });
        }
    }
    

