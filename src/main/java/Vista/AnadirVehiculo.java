    /**
     * Boton para eliminar vehiculo de la base de datos por medio del ID
     * @param evt 
     */
    private void jB_EliminarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_EliminarVehiculoActionPerformed
        // TODO add your handling code here:
        try{
            if(registeredIdVehiculo.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Por favor ingrese ID del Vehiculo.");
            }else{
                int id = Integer.valueOf(registeredIdVehiculo.getText());
                conexionVehiculo.eliminarVehiculo(id);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al eliminar el vehículo.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jB_EliminarVehiculoActionPerformed
    
