    /**
     * Boton para agregar vehiculo a la base de datos
     * @param evt 
     */
    private void jB_agregarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_agregarVehiculoActionPerformed
        // TODO add your handling code here:
        try{
            if(registeredClienteId.getText().isEmpty() || registeredModelo.getText().isEmpty() || registeredPlaca.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Por favor ingrese los datos del Vehiculo.");  
            }else{
                if(jCb_Marchamo.isSelected() && jCb_Rtv.isSelected()){
                    vehiculo.setMarchamo("Si");
                    vehiculo.setRtv("Si");
                    vehiculo.setClienteId(this.registeredClienteId.getText());
                    vehiculo.setModelo(this.registeredModelo.getText());
                    vehiculo.setPlaca(this.registeredPlaca.getText()); 
                    if(jCb_Golpes.isSelected()){
                        golpes = "Si";
                        vehiculo.setNotas(this.registeredNotas.getText());
                    }else{
                        golpes = "No";
                        vehiculo.setNotas("No presenta Golpes");
                    }
                    //Logica de los checbox de los servicios
                    if (jCb_cambioAceite.isSelected()) {
                        servicioBuilder.append("Aceite");
                    }
                    if (jCb_CambioFibras.isSelected()) {
                        if (servicioBuilder.length() > 0) {
                            servicioBuilder.append(" ");
                        }
                        servicioBuilder.append("Fibras");
                    }
                    if (jCb_halogenos.isSelected()) {
                        if (servicioBuilder.length() > 0) {
                            servicioBuilder.append(" ");
                        }
                        servicioBuilder.append("Halogenos");
                    }

                    vehiculo.setServicio(servicioBuilder.length() > 0 ? servicioBuilder.toString() : "No se realizaron servicios");

                    //Ingreso de vehiculo a la base de datos
                    conexionVehiculo.insertarVehiculo(vehiculo);

                    //System.out.print("funciono");
                }else{
                    JOptionPane.showMessageDialog(null,"No se Aceptan Vehiculos sin Marchamo o Rtv al Dia");            
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al agregar el vehículo.", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jB_agregarVehiculoActionPerformed
    
